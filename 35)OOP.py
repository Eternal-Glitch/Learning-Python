# OOPS concepts 

    # Class - A blueprint for creating objects (objects have properties and methods)
    # Object - An instance of a class (an object has state and behavior)
    # Method - A function defined inside a class
    # Attribute - A variable inside a class (a variable that belongs to the class)


    # Inheritance - A way to create new classes by inheriting properties and methods from existing classes
    # Encapsulation - A way to hide the implementation details of a class and only expose necessary functionalities
    # Polymorphism - A way to have the same method name but different implementations in different classes
    # Abstraction - A way to hide the complexities of a class and only show the necessary details to the user